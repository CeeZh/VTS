class ActionType(Enum):
    """Types of actions in the trajectory."""
    ZOOM_IN = "ZOOM_IN"
    ZOOM_OUT = "ZOOM_OUT"
    SHIFT = "SHIFT"
    ANSWER = "ANSWER"


@dataclass
class Node:
    """
    Represents a temporal segment in the hierarchical structure.

    Attributes:
        start_sec: Start time in seconds
        end_sec: End time in seconds
        frames: List of frame data (can be paths, tensors, or other representations)
        video_path: Path or identifier to the video file
        text_description: Textual description generated by VLM
        level: Hierarchy level (0 = root)
        parent: Parent node reference
        children: List of child nodes
        relevance_score: Relevance score computed by the scorer
        visited: Whether this node has been visited in the trajectory
        gt_coverage: Percentage of GT segment covered by this node (cached)
        iou: Intersection over Union with GT segment (cached)
        contains_gt_most: Whether this node contains GT most among siblings (cached)
        node_id: Index of this node among its siblings (0 for root)
    """
    start_sec: float
    end_sec: float
    frames: List[Any]
    video_path: Any = None
    text_description: str = ""
    level: int = 0
    parent: Optional['Node'] = None
    children: List['Node'] = field(default_factory=list)
    relevance_score: Optional[float] = None
    visited: bool = False
    gt_coverage: Optional[float] = None
    iou: Optional[float] = None
    contains_gt_most: bool = False
    node_id: int = 0
    predicted_interval: Optional[Tuple[float, float]] = None  # VLM-predicted clue interval
    predicted_iou: Optional[float] = None  # IoU between predicted interval and GT
    # Answer prediction fields
    predicted_answer_char: Optional[str] = None  # Character of predicted answer (A, B, C, D, etc.)
    predicted_answer: Optional[str] = None  # The predicted answer text
    predicted_answer_probs: Optional[Dict[str, float]] = None  # Probability for each choice keyed by char (e.g., {"A": 0.95, "B": 0.03})
    predicted_answer_correct: Optional[bool] = None  # Whether prediction matches GT
    
    def __repr__(self) -> str:
        return f"Node(id={self.node_id}, level={self.level}, [{self.start_sec:.1f}s, {self.end_sec:.1f}s])"

    def to_tree_dict(self) -> Dict[str, Any]:
        """Recursively serialize this node and its descendants to a plain dict."""
        result = {
            'node_id': self.node_id,
            'start_sec': self.start_sec,
            'end_sec': self.end_sec,
            'description': self.text_description or "",
            'visited': self.visited,
            'frames': self.frames if self.visited else [],
            'video_path': self.video_path,
        }
        if self.children:
            result['children'] = [child.to_tree_dict() for child in self.children]
        return result

    def duration(self) -> float:
        """Get the duration of this temporal segment."""
        return self.end_sec - self.start_sec
    
    def compute_gt_metrics(self, gt_node: 'Node') -> None:
        """
        Compute and cache GT coverage and IoU metrics for this node.

        Args:
            gt_node: Ground truth node representing the GT interval
        """
        node_interval = (self.start_sec, self.end_sec)
        gt_interval = (gt_node.start_sec, gt_node.end_sec)

        self.gt_coverage = compute_interval_coverage(node_interval, gt_interval)
        self.iou = compute_interval_iou(node_interval, gt_interval)
    
    @staticmethod
    def mark_gt_containing_siblings(siblings: List['Node'], parent_contains_gt: bool = True) -> None:
        """
        Mark which sibling contains GT most based on gt_coverage ranking.
        
        If parent doesn't contain GT, all children are marked as not containing GT.
        Otherwise, the child with highest gt_coverage is marked as containing GT.
        
        Args:
            siblings: List of sibling nodes
            parent_contains_gt: Whether parent contains GT (if False, all children marked False)
        """
        if not siblings:
            return
        
        # If parent doesn't contain GT, none of the children do
        if not parent_contains_gt:
            for sibling in siblings:
                sibling.contains_gt_most = False
            return
        
        # Find the child with maximum GT coverage
        max_coverage_node = max(siblings, key=lambda n: n.gt_coverage if n.gt_coverage is not None else 0.0)
        
        # Mark nodes
        for sibling in siblings:
            sibling.contains_gt_most = (sibling is max_coverage_node)
    
    def contains_gt(self) -> bool:
        """
        Check if this node contains GT most among its siblings.
        
        Uses the cached contains_gt_most flag which is set by comparing
        GT overlap among all sibling nodes.
        
        Returns:
            True if this node has the highest GT overlap among siblings
        """
        return self.contains_gt_most
    
    def is_similar_to_gt(self, gt_start: float, gt_end: float, threshold: float = 0.8) -> bool:
        """
        Check if this node is sufficiently similar to GT for termination.
        Uses IoU (Intersection over Union) metric.
        Uses cached iou if available, otherwise computes it.

        Args:
            gt_start: Ground truth start time
            gt_end: Ground truth end time
            threshold: IoU threshold (0.0 to 1.0)

        Returns:
            True if IoU with GT exceeds threshold
        """
        if self.iou is not None:
            return self.iou >= threshold

        # Fallback: compute IoU on the fly
        iou = compute_interval_iou(
            (self.start_sec, self.end_sec),
            (gt_start, gt_end)
        )
        return iou >= threshold


@dataclass
class Action:
    """
    Represents an action taken in the trajectory.

    Attributes:
        action_type: Type of action (ZOOM_IN, ZOOM_OUT, ANSWER)
        segment_id: For ZOOM_IN, the ID of the selected segment
        start_sec: For ZOOM_IN, the start of the selected segment
        end_sec: For ZOOM_IN, the end of the selected segment
        answer: For ANSWER, the final answer
        evidence: For ANSWER, the evidence (typically GT timestamps)
    """
    action_type: ActionType
    segment_id: Optional[int] = None
    start_sec: Optional[float] = None
    end_sec: Optional[float] = None
    answer: Optional[str] = None
    evidence: Optional[Dict[str, Any]] = None
    
    def __repr__(self) -> str:
        if self.action_type == ActionType.ZOOM_IN:
            return f"ZOOM_IN(segment={self.segment_id}, [{self.start_sec:.1f}s, {self.end_sec:.1f}s])"
            # return f"ZOOM_IN(segment={self.segment_id})"
        elif self.action_type == ActionType.ZOOM_OUT:
            return "ZOOM_OUT()"
        elif self.action_type == ActionType.SHIFT:
            return f"SHIFT(segment={self.segment_id}, [{self.start_sec:.1f}s, {self.end_sec:.1f}s])"
        elif self.action_type == ActionType.ANSWER:
            return f"ANSWER({self.answer})"
        return str(self.action_type)
    
    @staticmethod
    def zoom_in(start_sec: float, end_sec: float, segment_id: int) -> 'Action':
        """Create a ZOOM_IN action."""
        return Action(
            action_type=ActionType.ZOOM_IN,
            segment_id=segment_id,
            start_sec=start_sec,
            end_sec=end_sec
        )
    
    @staticmethod
    def zoom_out() -> 'Action':
        """Create a ZOOM_OUT action."""
        return Action(action_type=ActionType.ZOOM_OUT)

    @staticmethod
    def shift(start_sec: float, end_sec: float, segment_id: int) -> 'Action':
        """Create a SHIFT action (move to sibling segment)."""
        return Action(
            action_type=ActionType.SHIFT,
            segment_id=segment_id,
            start_sec=start_sec,
            end_sec=end_sec
        )

    @staticmethod
    def answer(answer: str, evidence: Dict[str, Any]) -> 'Action':
        """Create an ANSWER action."""
        return Action(
            action_type=ActionType.ANSWER,
            answer=answer,
            evidence=evidence
        )


@dataclass
class Observation:
    """
    Represents an observation of a temporal segment.
    
    Attributes:
        frames: Visual frames from the selected segment
        text_description: Textual description generated by VLM
        node: Reference to the corresponding node
    """
    frames: List[Any]
    text_description: str
    node: Node
    
    def __repr__(self) -> str:
        return f"Observation([{self.node.start_sec:.1f}s, {self.node.end_sec:.1f}s])"


@dataclass
class Reasoning:
    """
    Represents reasoning content explaining why an action is taken.

    Attributes:
        content: The reasoning text
        context: Additional context used to generate reasoning
        raw_response: Raw VLM response text before parsing (optional)
    """
    content: str
    context: Optional[Dict[str, Any]] = None
    raw_response: Optional[str] = None
    
    def __repr__(self) -> str:
        preview = self.content[:50] + "..." if len(self.content) > 50 else self.content
        return f"Reasoning(\"{preview}\")"


@dataclass
class Turn:
    """
    Represents a single turn in the trajectory.

    Attributes:
        reasoning: Reasoning content (can be None for initial observation)
        action: Action taken (can be None for initial observation)
        observation: Observation received
        memory: Memory prompt string (Tree Structure + Interaction History + Navigation Context)
    """
    reasoning: Optional[Reasoning]
    action: Optional[Action]
    observation: Observation
    memory: Optional[str] = None
    
    def __repr__(self) -> str:
        parts = []
        if self.reasoning:
            parts.append(f"R: {self.reasoning}")
        if self.action:
            parts.append(f"A: {self.action}")
        parts.append(f"O: {self.observation}")
        return " | ".join(parts)


class Trajectory:
    """
    Represents a complete trajectory consisting of multiple turns.
    
    A trajectory is: T = { O?, (R?, A?, O?), (R?, A?, O?), � }
    """
    
    def __init__(self, video_id: str, question: str, ground_truth: Dict[str, Any], gt_node: Optional[Node] = None):
        """
        Initialize a trajectory.
        
        Args:
            video_id: Identifier for the video
            question: The question to answer
            ground_truth: Ground truth information (timestamps, answer, etc.)
            gt_node: Optional ground truth node (for easy access to GT metrics)
        """
        self.video_id = video_id
        self.question = question
        self.ground_truth = ground_truth
        self.gt_node = gt_node
        self.turns: List[Turn] = []
        self.initial_observation: Optional[Observation] = None
        self.metadata: Dict[str, Any] = {}
    
    def set_initial_observation(self, observation: Observation) -> None:
        """Set the initial observation O?."""
        self.initial_observation = observation
        # Add as a turn with no reasoning or action
        self.turns.append(Turn(
            reasoning=None,
            action=None,
            observation=observation
        ))
    
    def add_turn(self, reasoning: Reasoning, action: Action, observation: Optional[Observation] = None, memory: Optional[str] = None) -> None:
        """
        Add a new turn to the trajectory.

        Args:
            reasoning: Reasoning for this turn
            action: Action taken
            observation: Observation received (None if action is ANSWER)
            memory: Memory prompt string at the time of this turn
        """
        self.turns.append(Turn(
            reasoning=reasoning,
            action=action,
            observation=observation,
            memory=memory
        ))
    
    def is_terminated(self) -> bool:
        """Check if the trajectory has terminated."""
        if not self.turns:
            return False
        last_turn = self.turns[-1]
        return (last_turn.action is not None and 
                last_turn.action.action_type == ActionType.ANSWER)
    
    def get_final_answer(self) -> Optional[str]:
        """Get the final answer if trajectory is terminated."""
        if self.is_terminated():
            return self.turns[-1].action.answer
        return None
    
    def to_dict(self) -> Dict[str, Any]:
        """
        Convert trajectory to dictionary format for serialization.
        
        Returns:
            Dictionary representation of the trajectory
        """
        return {
            'video_id': self.video_id,
            'question': self.question,
            'ground_truth': self.ground_truth,
            'turns': [
                {
                    'memory': turn.memory,
                    'reasoning': turn.reasoning.content if turn.reasoning else None,
                    'raw_response': turn.reasoning.raw_response if turn.reasoning else None,
                    'action': {
                        'type': turn.action.action_type.value if turn.action else None,
                        'segment_id': turn.action.segment_id if turn.action else None,
                        'start_sec': turn.action.start_sec if turn.action else None,
                        'end_sec': turn.action.end_sec if turn.action else None,
                        'answer': turn.action.answer if turn.action else None,
                        'evidence': turn.action.evidence if turn.action else None,
                    } if turn.action else None,
                    'observation': {
                        'node_id': turn.observation.node.node_id,
                        'start_sec': turn.observation.node.start_sec,
                        'end_sec': turn.observation.node.end_sec,
                        'text_description': turn.observation.text_description,
                        'level': turn.observation.node.level,
                    } if turn.observation else None,
                }
                for turn in self.turns
            ],
            'metadata': self.metadata,
        }
    
    def __repr__(self) -> str:
        status = "TERMINATED" if self.is_terminated() else "IN_PROGRESS"
        return f"Trajectory(video={self.video_id}, turns={len(self.turns)}, status={status})"
    
    def __len__(self) -> int:
        """Return the number of turns in the trajectory."""
        return len(self.turns)
